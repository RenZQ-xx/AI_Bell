import argparse
import itertools
import json
from collections import deque
from pathlib import Path


VAR_NAMES = ["A0", "A1", "B0", "B1", "C0", "C1"]
N_VARS = len(VAR_NAMES)
STATES = list(itertools.product([1, -1], repeat=N_VARS))
N_VERTICES = len(STATES)
STATE_TO_ID = {state: i for i, state in enumerate(STATES)}
ID_TO_STATE = {i: state for i, state in enumerate(STATES)}
IDX = {name: i for i, name in enumerate(VAR_NAMES)}


def create_permutation(transform_func):
    perm = []
    for i in range(N_VERTICES):
        new_state = transform_func(ID_TO_STATE[i])
        perm.append(STATE_TO_ID[new_state])
    return tuple(perm)


def op_abswap(state):
    values = list(state)
    values[IDX["A0"]], values[IDX["B0"]] = values[IDX["B0"]], values[IDX["A0"]]
    values[IDX["A1"]], values[IDX["B1"]] = values[IDX["B1"]], values[IDX["A1"]]
    return tuple(values)


def op_acswap(state):
    values = list(state)
    values[IDX["A0"]], values[IDX["C0"]] = values[IDX["C0"]], values[IDX["A0"]]
    values[IDX["A1"]], values[IDX["C1"]] = values[IDX["C1"]], values[IDX["A1"]]
    return tuple(values)


def op_flipin_a(state):
    values = list(state)
    values[IDX["A0"]], values[IDX["A1"]] = values[IDX["A1"]], values[IDX["A0"]]
    return tuple(values)


def op_flipin_b(state):
    values = list(state)
    values[IDX["B0"]], values[IDX["B1"]] = values[IDX["B1"]], values[IDX["B0"]]
    return tuple(values)


def op_flipin_c(state):
    values = list(state)
    values[IDX["C0"]], values[IDX["C1"]] = values[IDX["C1"]], values[IDX["C0"]]
    return tuple(values)


def make_flipout(var_name):
    target = IDX[var_name]

    def op(state):
        values = list(state)
        values[target] = -values[target]
        return tuple(values)

    return op


def build_generators():
    generators = [
        ("ABswap", create_permutation(op_abswap)),
        ("ACswap", create_permutation(op_acswap)),
        ("FlipIn_A", create_permutation(op_flipin_a)),
        ("FlipIn_B", create_permutation(op_flipin_b)),
        ("FlipIn_C", create_permutation(op_flipin_c)),
    ]
    for var_name in VAR_NAMES:
        generators.append((f"FlipOut_{var_name}", create_permutation(make_flipout(var_name))))
    return generators


def compose(left, right):
    # Return left o right, meaning apply right first, then left.
    return tuple(left[right[i]] for i in range(len(left)))


def permutation_to_cycles(perm, one_based=False):
    visited = [False] * len(perm)
    cycles = []
    offset = 1 if one_based else 0

    for start in range(len(perm)):
        if visited[start] or perm[start] == start:
            visited[start] = True
            continue

        cycle = []
        current = start
        while not visited[current]:
            visited[current] = True
            cycle.append(current + offset)
            current = perm[current]
        cycles.append(tuple(cycle))

    return cycles


def format_cycles(perm, one_based=False):
    cycles = permutation_to_cycles(perm, one_based=one_based)
    if not cycles:
        return "()"
    return "".join("(" + " ".join(str(x) for x in cycle) + ")" for cycle in cycles)


def serialize_cycles(perm, one_based=False):
    return [list(cycle) for cycle in permutation_to_cycles(perm, one_based=one_based)]


def enumerate_group(generators):
    identity = tuple(range(N_VERTICES))
    seen = {identity: "e"}
    queue = deque([identity])

    while queue:
        current = queue.popleft()
        current_word = seen[current]
        for gen_name, gen_perm in generators:
            next_perm = compose(gen_perm, current)
            if next_perm in seen:
                continue
            seen[next_perm] = gen_name if current_word == "e" else f"{gen_name} * {current_word}"
            queue.append(next_perm)

    return seen


def summarize_defined_symmetries():
    lines = [
        "Defined generators on the 64-state vertex set induced by (+/-1)^6:",
        "1. ABswap: swap (A0, A1) with (B0, B1)",
        "2. ACswap: swap (A0, A1) with (C0, C1)",
        "3. FlipIn_A: swap A0 and A1",
        "4. FlipIn_B: swap B0 and B1",
        "5. FlipIn_C: swap C0 and C1",
        "6. FlipOut_A0, FlipOut_A1, FlipOut_B0, FlipOut_B1, FlipOut_C0, FlipOut_C1:",
        "   flip the sign of the corresponding coordinate",
    ]
    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(
        description="Enumerate the group generated by the symmetries defined in src/experiments/322/nauty."
    )
    parser.add_argument(
        "--one-based",
        action="store_true",
        help="Print cycle notation using 1-based vertex labels instead of 0-based.",
    )
    parser.add_argument(
        "--show-states",
        action="store_true",
        help="Also print the 64 vertex labels as state tuples.",
    )
    parser.add_argument(
        "--txt-out",
        type=Path,
        help="Write all group elements and their cycle decompositions to a text file.",
    )
    parser.add_argument(
        "--json-out",
        type=Path,
        help="Write all group elements and their cycle decompositions to a JSON file.",
    )
    parser.add_argument(
        "--summary-only",
        action="store_true",
        help="Print only the summary and generators to stdout.",
    )
    args = parser.parse_args()

    generators = build_generators()
    elements = enumerate_group(generators)
    ordered_perms = sorted(elements.keys())

    print(summarize_defined_symmetries())
    print()
    print(f"Vertex set size: {N_VERTICES}")
    print(f"Number of generators: {len(generators)}")
    print(f"Group order: {len(ordered_perms)}")
    print("This is consistent with 2^6 x (S3 acting on {A,B,C}) x (C2)^3, so the generators do form a finite group.")

    if args.show_states:
        print()
        print("Vertex labels:")
        for vertex_id, state in enumerate(STATES, start=1 if args.one_based else 0):
            print(f"{vertex_id}: {state}")

    print()
    print("Generators:")
    for name, perm in generators:
        print(f"- {name}: {format_cycles(perm, one_based=args.one_based)}")

    group_records = []
    for idx, perm in enumerate(ordered_perms):
        group_records.append(
            {
                "index": idx,
                "word": elements[perm],
                "permutation": list(perm),
                "cycles": serialize_cycles(perm, one_based=args.one_based),
                "cycle_notation": format_cycles(perm, one_based=args.one_based),
            }
        )

    if args.txt_out is not None:
        args.txt_out.parent.mkdir(parents=True, exist_ok=True)
        with args.txt_out.open("w", encoding="utf-8") as fh:
            fh.write(summarize_defined_symmetries())
            fh.write("\n\n")
            fh.write(f"Vertex set size: {N_VERTICES}\n")
            fh.write(f"Number of generators: {len(generators)}\n")
            fh.write(f"Group order: {len(ordered_perms)}\n\n")
            fh.write("Generators:\n")
            for name, perm in generators:
                fh.write(f"- {name}: {format_cycles(perm, one_based=args.one_based)}\n")
            fh.write("\nGroup elements:\n")
            for record in group_records:
                fh.write(f"[{record['index']:04d}] word = {record['word']}\n")
                fh.write(f"        cycles = {record['cycle_notation']}\n")

    if args.json_out is not None:
        args.json_out.parent.mkdir(parents=True, exist_ok=True)
        payload = {
            "var_names": VAR_NAMES,
            "vertex_set_size": N_VERTICES,
            "number_of_generators": len(generators),
            "group_order": len(ordered_perms),
            "one_based": args.one_based,
            "generators": [
                {
                    "name": name,
                    "permutation": list(perm),
                    "cycles": serialize_cycles(perm, one_based=args.one_based),
                    "cycle_notation": format_cycles(perm, one_based=args.one_based),
                }
                for name, perm in generators
            ],
            "elements": group_records,
        }
        with args.json_out.open("w", encoding="utf-8") as fh:
            json.dump(payload, fh, ensure_ascii=False, indent=2)

    if not args.summary_only:
        print()
        print("Group elements:")
        for record in group_records:
            print(f"[{record['index']:04d}] word = {record['word']}")
            print(f"        cycles = {record['cycle_notation']}")


if __name__ == "__main__":
    main()
